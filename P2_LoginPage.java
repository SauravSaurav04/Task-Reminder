    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        if (validatee()) {

            DbClass d = new DbClass();

            if (d.searchData(jTextField1.getText(), jPasswordField1.getText())) {

                email = jTextField1.getText();
                password = jPasswordField1.getText();
                name = d.getDataNameFromTable(email, password);

                new P3_MainPage(name, email).setVisible(true);
                dispose();
            } else {
                jTextField1.setText("");
                jPasswordField1.setText("");
                JOptionPane.showMessageDialog(P2_LoginPage.this, "wrong detail");
                jTextField1.grabFocus();
            }
            reset();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    void reset(){
        jTextField1.setText("");
        jPasswordField1.setText("");
    }
    private boolean validatee() {
        if (jTextField1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(P2_LoginPage.this, "Enter email");
            jTextField1.grabFocus();
            return false;
        }
        if (jPasswordField1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(P2_LoginPage.this, "Enter password");
            jPasswordField1.grabFocus();
            return false;
        }
        return true;
    }
