    private void jLabel6MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel6MouseClicked
        // TODO add your handling code here:
        if(jTextField1.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(P2_LoginPage.this, "Enter Email");
            jTextField1.grabFocus();
            return;
        }
        DbClass db = new DbClass();
        if(db.searchEmail(jTextField1.getText())){
            new P2_ForgotPassword(jTextField1.getText()).setVisible(true);
            dispose();
        } else {
            JOptionPane.showMessageDialog(P2_LoginPage.this, "Invalid Email");
            jTextField1.grabFocus();
            return;
        }
    }//GEN-LAST:event_jLabel6MouseClicked

