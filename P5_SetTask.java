    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(validateee()){
            subject = jTextField1.getText();
            hourNeed = Double.parseDouble(jTextField4.getText());
            QuestionHave = Integer.parseInt(jTextField2.getText());
            endDate = changeDateFormat(jDateChooser1.getDate());
            dsHours = Double.parseDouble(jTextField5.getText());
            
            LocalDate l = LocalDate.now();
            String todayDate = l.toString();
            
            Calculations c = new Calculations();
            int totalDay = c.calcutateDate(todayDate, endDate);
            
            dqsolve = Integer.parseInt(jTextField3.getText());
            
            String s = "Your daily study hours are less Please set daily study hours greater than ";
            s = s.concat(String.format("%.2f",(double)QuestionHave/totalDay));
            
            if(dqsolve >= (double)QuestionHave/totalDay){
                DbClass db = new DbClass();
//                db.createSetTaskTable();
                db.insertDataInSetTaskTable(name, email, subject, hourNeed, QuestionHave, endDate, dsHours, dqsolve);
                JOptionPane.showMessageDialog(P5_SetTask.this, "Task added successfully");
            } else {
                JOptionPane.showMessageDialog(P5_SetTask.this, s);
                jTextField3.grabFocus();
                return;
            }
        }
        reset();
    }//GEN-LAST:event_jButton1ActionPerformed

