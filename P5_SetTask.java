    private String changeDateFormat(Date temp){
//        SimpleDateFormat dtFormat = new SimpleDateFormat("dd/MM/yyyy");
        SimpleDateFormat dtFormat = new SimpleDateFormat("yyyy-MM-dd");
        try {
            return dtFormat.format(temp);
        } catch (Exception e) {
            e.printStackTrace();
        }
        return "";
    }
    
    private boolean validateee(){
        if(jTextField1.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(P5_SetTask.this, "Enter Subject Name");
            jTextField1.grabFocus();
            return false;
        }
        if(jTextField4.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(P5_SetTask.this, "Enter How many hour this subject need");
            jTextField4.grabFocus();
            return false;
        }
        if(jTextField2.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(P5_SetTask.this, "Enter How many question this subject have");
            jTextField2.grabFocus();
            return false;
        }
        if(jTextField5.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(P5_SetTask.this, "Enter How many hours you study per day");
            jTextField5.grabFocus();
            return false;
        }
        if(jTextField3.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(P5_SetTask.this, "Enter How many question you solve per day");
            jTextField3.grabFocus();
            return false;
        }
        if(jDateChooser1.getDate()==null){
            JOptionPane.showMessageDialog(P5_SetTask.this, "Select end Date");
            jDateChooser1.grabFocus();
            return false;
        }
        return true;
    }
